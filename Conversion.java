public class Conversion
{
	public static void main (String[] args)
	{
		double Inch = 2.54;
		double Yard = 0.9144;
		double Mile = 1.609;
		double Centimeter = 0.3937;
		double Meter = 1.094;
		double Kilometer = 0.6214;
		double LightYear = 9.464E12;
		System.out.println("        Units and Conversion Factors");
		System.out.println("--------------------------------------------");
		System.out.println("inch = " + Inch + " centimeters");
		System.out.println("yard = " + Yard + " meter");
		System.out.println("mile = " + Mile + " kilometers");
		System.out.println("centimeter = " + Centimeter + " inch");
		System.out.println("meter = " + Meter + " yards");
		System.out.println("kilometer = " + Kilometer + " mile");
		System.out.println("light year = " + LightYear + " kilometers");
	}
}